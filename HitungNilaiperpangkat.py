# menghitung nlai perpangkatan  

nilai = int(input("Masukkan angka = ")) 
pangkat = int(input("Masukkan pangkat = ")) 
a = 1

while a <= pangkat:
    if a <= pangkat:
        print(nilai , end =   " x ")
        a = a + 1
    elif a == pangkat:
        print(nilai , end =   " = ")
        a = a + 1

sum = nilai * pangkat
print(sum)

         

    
        